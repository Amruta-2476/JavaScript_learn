/*  API
 * when you want to pass your work to someone else, we should use API
 * eg: API is like a waiter in a restaurant, you tell the waiter what you want and the waiter gets it for you
 * 
 * 
 * You (the client) tell the waiter (the API) what you want (your request), and the waiter brings back what you asked for from the kitchen (the server).
 * 
 * 
 * How Does an API Work?
Request: You (the client) make a request to the server through the API. For example, if you're using a weather app, you might request the current weather data for your location.
Process: The API takes your request and tells the server what you need.
Response: The server processes the request, retrieves the needed data, and sends it back to the API.
Deliver: The API then delivers this data back to you.
 * 

 * Let's say you want to get data from a website using an API. You can use JavaScript's fetch function to make a request.
 * 
 * 
 * we get some values from backend, we can use fetch to get the data. Values come in the form of JSON
 * 
 * JSON is often used to send data between a server and a web application as text.
 * 
 * JSON data is represented as key-value pairs. The data can be organized in two main structures:
Objects: Represented as curly braces {}, containing key-value pairs.
Arrays: Represented as square brackets [], containing a list of values.
 */
